function Set_Sess(L, B) {
    sessionStorage.setItem("precedence_L", L);
    sessionStorage.setItem("precedence_B", B);
    sessionStorage.removeItem("precedence_C");
    sessionStorage.removeItem("precedence_V");

    window.location = "./";
}
